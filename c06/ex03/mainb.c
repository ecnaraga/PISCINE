#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}


int	main(int argc, char **argv)
{
	if (argc == 3)
	{
		ft_i(argv[1], argv[2]);
	}
	ft_putchar('\n');
	return (0);
}
